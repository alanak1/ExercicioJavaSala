
// atributos
public class Alunos {
    private String nome;
    private int codigoId;

    // construtor
    public Alunos(String nome, int codigoId){
        this.nome = nome;
        this.codigoId = codigoId;
    }
    

public String getNome() {
    return nome;
}
public void setNome(String nome) {
    this.nome = nome;
}
public int getCodigoId() {
return codigoId;
}

}
